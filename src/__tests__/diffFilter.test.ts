describe('normalizeGitLabDiffResponse', () => {
  it('returns plain unified diff text unchanged (not JSON)', () => {
    const diff = tsSection('src/foo.ts');
    expect(normalizeGitLabDiffResponse(diff)).toBe(diff);
  });

  it('returns non-GitLab JSON unchanged', () => {
    const json = JSON.stringify({ foo: 'bar', baz: 42 });
    expect(normalizeGitLabDiffResponse(json)).toBe(json);
  });

  it('returns JSON array without "diff" fields unchanged', () => {
    const json = JSON.stringify([{ name: 'a' }, { name: 'b' }]);
    expect(normalizeGitLabDiffResponse(json)).toBe(json);
  });

  it('converts a single-file GitLab API array to unified diff', () => {
    const apiResponse = JSON.stringify([
      {
        old_path: 'src/foo.ts',
        new_path: 'src/foo.ts',
        diff: '@@ -1,3 +1,4 @@\n const x = 1;\n+console.log("hi");\n const y = 2;\n const z = 3;\n',
        new_file: false,
        deleted_file: false,
      },
    ]);
    const result = normalizeGitLabDiffResponse(apiResponse);
    expect(result).toContain('diff --git a/src/foo.ts b/src/foo.ts');
    expect(result).toContain('--- a/src/foo.ts');
    expect(result).toContain('+++ b/src/foo.ts');
    expect(result).toContain('@@ -1,3 +1,4 @@');
    expect(result).toContain('+console.log("hi");');
  });

  it('converts a multi-file GitLab API array to two diff sections', () => {
    const apiResponse = JSON.stringify([
      {
        old_path: 'src/foo.ts',
        new_path: 'src/foo.ts',
        diff: '@@ -1 +1 @@\n-old\n+new\n',
        new_file: false,
        deleted_file: false,
      },
      {
        old_path: 'src/bar.ts',
        new_path: 'src/bar.ts',
        diff: '@@ -5 +5 @@\n-x\n+y\n',
        new_file: false,
        deleted_file: false,
      },
    ]);
    const result = normalizeGitLabDiffResponse(apiResponse);
    expect(result).toContain('diff --git a/src/foo.ts b/src/foo.ts');
    expect(result).toContain('diff --git a/src/bar.ts b/src/bar.ts');
  });

  it('handles the {diffs:[...]} wrapper form', () => {
    const apiResponse = JSON.stringify({
      diffs: [
        {
          old_path: 'src/baz.ts',
          new_path: 'src/baz.ts',
          diff: '@@ -1 +1 @@\n-a\n+b\n',
          new_file: false,
          deleted_file: false,
        },
      ],
    });
    const result = normalizeGitLabDiffResponse(apiResponse);
    expect(result).toContain('diff --git a/src/baz.ts b/src/baz.ts');
    expect(result).toContain('@@ -1 +1 @@');
  });

  it('handles new_file: true with correct headers', () => {
    const apiResponse = JSON.stringify([
      {
        old_path: 'src/new.ts',
        new_path: 'src/new.ts',
        diff: '@@ -0,0 +1,2 @@\n+export const x = 1;\n+export const y = 2;\n',
        new_file: true,
        deleted_file: false,
      },
    ]);
    const result = normalizeGitLabDiffResponse(apiResponse);
    expect(result).toContain('diff --git a/dev/null b/src/new.ts');
    expect(result).toContain('new file mode 100644');
    expect(result).toContain('--- /dev/null');
    expect(result).toContain('+++ b/src/new.ts');
  });

  it('handles deleted_file: true with correct +++ header', () => {
    const apiResponse = JSON.stringify([
      {
        old_path: 'src/gone.ts',
        new_path: 'src/gone.ts',
        diff: '@@ -1,2 +0,0 @@\n-export const x = 1;\n-export const y = 2;\n',
        new_file: false,
        deleted_file: true,
      },
    ]);
    const result = normalizeGitLabDiffResponse(apiResponse);
    expect(result).toContain('diff --git a/src/gone.ts b/src/gone.ts');
    expect(result).toContain('+++ /dev/null');
  });

  it('integration: output feeds into splitDiffByFile correctly', () => {
    const apiResponse = JSON.stringify([
      {
        old_path: 'src/alpha.ts',
        new_path: 'src/alpha.ts',
        diff: '@@ -1,3 +1,4 @@\n const a = 1;\n+const b = 2;\n const c = 3;\n const d = 4;\n',
        new_file: false,
        deleted_file: false,
      },
      {
        old_path: 'src/beta.ts',
        new_path: 'src/beta.ts',
        diff: '@@ -10,3 +10,4 @@\n const x = 10;\n+const y = 11;\n const z = 12;\n',
        new_file: false,
        deleted_file: false,
      },
    ]);
    const unified = normalizeGitLabDiffResponse(apiResponse);
    const sections = splitDiffByFile(unified);
    expect(sections).toHaveLength(2);
    expect(sections[0].filePath).toBe('src/alpha.ts');
    expect(sections[1].filePath).toBe('src/beta.ts');
    expect(sections[0].isNewFile).toBe(false);
  });

  it('integration: new_file entry is detected by splitDiffByFile', () => {
    const apiResponse = JSON.stringify([
      {
        old_path: 'src/brand_new.ts',
        new_path: 'src/brand_new.ts',
        diff: '@@ -0,0 +1 @@\n+export const init = true;\n',
        new_file: true,
        deleted_file: false,
      },
    ]);
    const unified = normalizeGitLabDiffResponse(apiResponse);
    const sections = splitDiffByFile(unified);
    expect(sections).toHaveLength(1);
    expect(sections[0].isNewFile).toBe(true);
    expect(sections[0].filePath).toBe('src/brand_new.ts');
  });
});

