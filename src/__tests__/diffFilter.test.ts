describe('splitDiffByFile', () => {
  it('splits a multi-file diff into individual sections', () => {
    const diff = makeDiff(
      tsSection('src/foo.ts'),
      tsSection('src/bar.ts'),
      tsSection('src/baz.ts')
    );

    const sections = splitDiffByFile(diff);

    expect(sections).toHaveLength(3);
    expect(sections[0].filePath).toBe('src/foo.ts');
    expect(sections[1].filePath).toBe('src/bar.ts');
    expect(sections[2].filePath).toBe('src/baz.ts');
  });

  it('excludes filtered files from the split result', () => {
    const diff = makeDiff(
      tsSection('src/foo.ts'),
      lockSection(),
      tsSection('src/bar.ts')
    );

    const sections = splitDiffByFile(diff);

    expect(sections).toHaveLength(2);
    expect(sections.map(s => s.filePath)).toEqual(['src/foo.ts', 'src/bar.ts']);
  });

  it('includes the full diff content for each section', () => {
    const diff = tsSection('src/foo.ts');
    const sections = splitDiffByFile(diff);

    expect(sections).toHaveLength(1);
    expect(sections[0].diff).toContain('diff --git');
    expect(sections[0].diff).toContain("console.log('hello')");
  });

  it('returns empty array for diff with only non-reviewable files', () => {
    const diff = makeDiff(lockSection(), binarySection());
    const sections = splitDiffByFile(diff);

    expect(sections).toHaveLength(0);
  });

  it('returns empty array for empty diff', () => {
    const sections = splitDiffByFile('');
    expect(sections).toHaveLength(0);
  });

  it('sets isNewFile=true for new files (diff --git a/dev/null)', () => {
    const diff = newFileSection('src/auth.ts');
    const sections = splitDiffByFile(diff);

    expect(sections).toHaveLength(1);
    expect(sections[0].isNewFile).toBe(true);
    expect(sections[0].filePath).toBe('src/auth.ts');
  });

  it('sets isNewFile=false for existing file changes', () => {
    const diff = tsSection('src/foo.ts');
    const sections = splitDiffByFile(diff);

    expect(sections).toHaveLength(1);
    expect(sections[0].isNewFile).toBe(false);
  });

  it('correctly mixes new and existing files in split results', () => {
    const diff = makeDiff(
      newFileSection('src/auth.ts'),
      tsSection('src/foo.ts'),
      newFileSection('src/utils.ts'),
      tsSection('src/bar.ts')
    );
    const sections = splitDiffByFile(diff);

    expect(sections).toHaveLength(4);
    expect(sections[0].isNewFile).toBe(true);
    expect(sections[1].isNewFile).toBe(false);
    expect(sections[2].isNewFile).toBe(true);
    expect(sections[3].isNewFile).toBe(false);
  });
});

