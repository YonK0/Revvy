// src/__tests__/diffFilter.test.ts
// Unit tests for diffFilter — split helpers and filter rules.
// No VS Code host or mocks needed — this module is pure functions.

import { describe, it, expect } from 'vitest';
import { filterDiff, splitDiffByFile, isNewFile, normalizeGitLabDiffResponse, normalizeRemoteDiff } from '../diffFilter';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function makeDiff(...sections: string[]): string {
  return sections.join('\n');
}

function tsSection(name = 'src/foo.ts'): string {
  return `diff --git a/${name} b/${name}
--- a/${name}
+++ b/${name}
@@ -1,3 +1,4 @@
 const x = 1;
+console.log('hello');
 const y = 2;
 const z = 3;`;
}

function lockSection(): string {
  return `diff --git a/package-lock.json b/package-lock.json
--- a/package-lock.json
+++ b/package-lock.json
@@ -1,5 +1,5 @@
 {
-  "version": "1.0.0",
+  "version": "1.0.1",
   "lockfileVersion": 3,
 }`;
}

function yarnLockSection(): string {
  return `diff --git a/yarn.lock b/yarn.lock
--- a/yarn.lock
+++ b/yarn.lock
@@ -1,3 +1,3 @@
-# yarn lockfile v1
+# yarn lockfile v2`;
}

function cargoLockSection(): string {
  return `diff --git a/Cargo.lock b/Cargo.lock
--- a/Cargo.lock
+++ b/Cargo.lock
@@ -1,5 +1,5 @@
 # This file is automatically @generated by Cargo.
 # It is not intended for manual editing.
-version = 3
+version = 4`;
}

function binarySection(): string {
  return `diff --git a/assets/icon.png b/assets/icon.png
Binary files a/assets/icon.png and b/assets/icon.png differ`;
}

function generatedPbSection(): string {
  return `diff --git a/src/generated/user.pb.ts b/src/generated/user.pb.ts
--- a/src/generated/user.pb.ts
+++ b/src/generated/user.pb.ts
@@ -1,3 +1,4 @@
 // Generated by protoc
+const new_field = 1;`;
}

function minifiedSection(): string {
  return `diff --git a/dist/bundle.min.js b/dist/bundle.min.js
--- a/dist/bundle.min.js
+++ b/dist/bundle.min.js
@@ -1 +1 @@
-!function(e){...}
+!function(e){...updated...}`;
}

function changelogSection(): string {
  return `diff --git a/CHANGELOG.md b/CHANGELOG.md
--- a/CHANGELOG.md
+++ b/CHANGELOG.md
@@ -1,3 +1,5 @@
 # Changelog
+## 1.0.1
+- Fixed build`;
}

function coverageSection(): string {
  return `diff --git a/coverage/lcov.info b/coverage/lcov.info
--- a/coverage/lcov.info
+++ b/coverage/lcov.info
@@ -1,3 +1,3 @@
-TN:
-SF:src/foo.ts
+TN:updated
+SF:src/foo.ts`;
}

function newFileSection(name = 'src/newModule.ts'): string {
  return `diff --git a/dev/null b/${name}
new file mode 100644
index 0000000..e69de29
--- /dev/null
+++ b/${name}
@@ -0,0 +1,3 @@
+export const foo = 1;
+export const bar = 2;
+export const baz = 3;`;
}

// ---------------------------------------------------------------------------
// Tests — filterDiff
// ---------------------------------------------------------------------------

describe('filterDiff', () => {
  it('returns the full diff when all files are reviewable', () => {
    const diff = makeDiff(tsSection('src/foo.ts'), tsSection('src/bar.ts'));
    const result = filterDiff(diff);

    expect(result.keptFiles).toBe(2);
    expect(result.skippedFiles).toBe(0);
    expect(result.filteredDiff).toContain('src/foo.ts');
    expect(result.filteredDiff).toContain('src/bar.ts');
  });

  it('removes package-lock.json sections', () => {
    const diff = makeDiff(tsSection('src/foo.ts'), lockSection());
    const result = filterDiff(diff);

    expect(result.keptFiles).toBe(1);
    expect(result.skippedFiles).toBe(1);
    expect(result.skippedFilePaths).toContain('package-lock.json');
    expect(result.filteredDiff).not.toContain('package-lock.json');
  });

  it('removes yarn.lock sections', () => {
    const diff = makeDiff(yarnLockSection(), tsSection('src/app.ts'));
    const result = filterDiff(diff);

    expect(result.skippedFiles).toBe(1);
    expect(result.skippedFilePaths).toContain('yarn.lock');
  });

  it('removes Cargo.lock sections', () => {
    const diff = makeDiff(cargoLockSection(), tsSection('src/main.ts'));
    const result = filterDiff(diff);

    expect(result.skippedFiles).toBe(1);
    expect(result.skippedFilePaths).toContain('Cargo.lock');
  });

  it('removes binary file sections', () => {
    const diff = makeDiff(binarySection(), tsSection('src/util.ts'));
    const result = filterDiff(diff);

    expect(result.skippedFiles).toBe(1);
    expect(result.skippedFilePaths).toContain('assets/icon.png');
  });

  it('removes generated protobuf files', () => {
    const diff = makeDiff(generatedPbSection(), tsSection('src/handlers.ts'));
    const result = filterDiff(diff);

    expect(result.skippedFiles).toBe(1);
    expect(result.skippedFilePaths[0]).toContain('user.pb.ts');
  });

  it('removes minified JS files', () => {
    const diff = makeDiff(minifiedSection(), tsSection('src/index.ts'));
    const result = filterDiff(diff);

    expect(result.skippedFiles).toBe(1);
    expect(result.skippedFilePaths[0]).toContain('bundle.min.js');
  });

  it('removes CHANGELOG.md sections', () => {
    const diff = makeDiff(changelogSection(), tsSection('src/app.ts'));
    const result = filterDiff(diff);

    expect(result.skippedFiles).toBe(1);
    expect(result.skippedFilePaths).toContain('CHANGELOG.md');
  });

  it('removes coverage report sections', () => {
    const diff = makeDiff(coverageSection(), tsSection('src/app.ts'));
    const result = filterDiff(diff);

    expect(result.skippedFiles).toBe(1);
    expect(result.skippedFilePaths[0]).toContain('lcov.info');
  });

  it('removes multiple files in a single diff', () => {
    const diff = makeDiff(
      lockSection(),
      tsSection('src/foo.ts'),
      yarnLockSection(),
      binarySection(),
      tsSection('src/bar.ts'),
      generatedPbSection()
    );
    const result = filterDiff(diff);

    expect(result.keptFiles).toBe(2);
    expect(result.skippedFiles).toBe(4);
    expect(result.skippedFilePaths).toContain('package-lock.json');
    expect(result.skippedFilePaths).toContain('yarn.lock');
    expect(result.skippedFilePaths[2]).toContain('icon.png');
    expect(result.skippedFilePaths[3]).toContain('user.pb.ts');
  });

  it('reports estimated token savings', () => {
    const lockContent = lockSection();
    const diff = makeDiff(lockContent, tsSection('src/foo.ts'));
    const result = filterDiff(diff);

    expect(result.estimatedTokensSaved).toBeGreaterThan(0);
    // Rough calculation: ~4 chars per token
    expect(result.estimatedTokensSaved).toBe(Math.round(lockContent.length / 4));
  });

  it('tracks both kept and skipped file paths', () => {
    const diff = makeDiff(
      lockSection(),
      tsSection('src/foo.ts'),
      binarySection(),
      tsSection('src/bar.ts')
    );
    const result = filterDiff(diff);

    expect(result.keptFilePaths).toEqual(['src/foo.ts', 'src/bar.ts']);
    expect(result.skippedFilePaths).toContain('package-lock.json');
    expect(result.skippedFilePaths).toContain('assets/icon.png');
  });

  it('handles empty diff gracefully', () => {
    const result = filterDiff('');

    // Empty string produces one non-matching section that gets kept
    expect(result.keptFiles).toBe(1);
    expect(result.skippedFiles).toBe(0);
    expect(result.filteredDiff.trim()).toBe('');
  });

  it('handles diff with only non-reviewable files', () => {
    const diff = makeDiff(lockSection(), binarySection());
    const result = filterDiff(diff);

    expect(result.keptFiles).toBe(0);
    expect(result.skippedFiles).toBe(2);
    expect(result.filteredDiff.trim()).toBe('');
  });
});

