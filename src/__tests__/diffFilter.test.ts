describe('isNewFile', () => {
  it('returns true for "new file mode" marker', () => {
    const section = `diff --git a/dev/null b/src/foo.ts
new file mode 100644
--- /dev/null
+++ b/src/foo.ts`;
    expect(isNewFile(section)).toBe(true);
  });

  it('returns true for "a/dev/null" in diff header', () => {
    const section = `diff --git a/dev/null b/src/bar.ts
index 0000000..e69de29
--- /dev/null
+++ b/src/bar.ts`;
    expect(isNewFile(section)).toBe(true);
  });

  it('returns false for existing file changes', () => {
    const section = `diff --git a/src/foo.ts b/src/foo.ts
--- a/src/foo.ts
+++ b/src/foo.ts`;
    expect(isNewFile(section)).toBe(false);
  });

  it('returns false for empty string', () => {
    expect(isNewFile('')).toBe(false);
  });
});

